# coding: utf-8
from pygame.locals import *
import pygame

#-------------------------------------------------------------------------------
# マップ上のブロックのベースクラス
#-------------------------------------------------------------------------------
class BlockBase:

    #-------------------------------------------------------------------------------
    # Public
    #-------------------------------------------------------------------------------
    can_walk = None
    obj_type = None
    color    = None
    image    = None

    #-------------------------------------------------------------------------------
    # Private
    #-------------------------------------------------------------------------------
    __pygame = None

    #-------------------------------------------------------------------------------
    # コンストラクタ
    #-------------------------------------------------------------------------------
    def __init__( self, pygame, color, obj_type, can_walk, file ):

        self.__pygame = pygame
        self.can_walk = can_walk
        self.obj_type = obj_type
        self.color    = color
        self.image    = self.__pygame.image.load( file )

        return
